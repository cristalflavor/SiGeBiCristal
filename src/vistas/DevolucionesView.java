    private void jtfIdSocioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfIdSocioKeyReleased
        // TODO add your handling code here:
        limpiarLabels();
        limpiarTabla();
        jLFoto.setVisible(false);
        if(jtfIdSocio.getText().equals("")){limpiarLabels();
            limpiarTabla();
            jLFoto.setVisible(false);
        }else{
            for(Socio socio:socios){
                if(String.valueOf(socio.getIdSocio()).equals(jtfIdSocio.getText())){
                    jlApellido.setText("Apellido: "+socio.getApellido());
                    jlNombre.setText("Nombre: "+socio.getNombre());
                    jlDireccion.setText("Direccion: "+socio.getDomicilio());
                    jlMail.setText("E-Mail: "+socio.getMail());
                    jLFoto.setVisible(true);
                    cargarFoto(socio.getIdSocio());
                    cargarDev(socio);
                }
            }
        }
    }//GEN-LAST:event_jtfIdSocioKeyReleased

    private void jbtDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtDevolverActionPerformed
        // TODO add your handling code here:
        
        if(jtbDevoluciones.getSelectedRow()>-1){
            int idej=jtbDevoluciones.getSelectedRow();
            int cel=Integer.parseInt(jtbDevoluciones.getValueAt(idej, 0).toString());
            PrestamoData pd=new PrestamoData();
            pd.devolucionPrestamo(cel);
            Socio socio=new Socio();
            socio.setIdSocio(Integer.parseInt(jtfIdSocio.getText()));
            limpiarTabla();
            cargarDev(socio);
        }else{JOptionPane.showMessageDialog(null, "Debe elegir un prestamo de la tabla");}
    }//GEN-LAST:event_jbtDevolverActionPerformed

    private void limpiarLabels(){
        jlApellido.setText("Apellido: ");
                jlNombre.setText("Nombre: ");
                jlDireccion.setText("Direccion: ");
                jlMail.setText("E-Mail: ");
    }
    private void cargarSocios(){
        SocioData sd= new SocioData();
        socios=sd.buscarHistorialSocios("Estado", "1");
    }
    
    private void cargarTabla(){
        modelo.addColumn("Codigo");
        modelo.addColumn("Socio");
        modelo.addColumn("Ejemplar");
        modelo.addColumn("Fecha Pres.");
        modelo.addColumn("Fecha Dev.");
        jtbDevoluciones.setModel(modelo);
    }
    private void limpiarTabla(){
        int f=modelo.getRowCount()-1;
        for(;f>=0;f--){
            modelo.removeRow(f);
        }
    }
    
    private void cargarDev(Socio socio){
        PrestamoData pd=new PrestamoData();
        prestamos=null;
        prestamos=pd.listarPrestamosSuperCargado(socio.getIdSocio());
        cargarTablaDev();
    }
    
    private void cargarTablaDev(){
        for(Prestamo prestamo:prestamos){
            modelo.addRow(new Object[]{prestamo.getIdPrestamo(),prestamo.getLector().getIdSocio(),prestamo.getEjemplar().getLibro().getTitulo(),prestamo.getFechaInicio(),prestamo.getFechaFin()});
        }
    }
    
    private void cargarFoto(int idSocio){
        try {
            File file = new File("./src/vistas/imagenes/foto_" + idSocio + ".jpg");
            //Se  lee una imagen desde un archivo y se almacena en una variable de tipo BufferedImage
            BufferedImage fotoPerfil = ImageIO.read(file);
            
            //Se borra el texto POR DEFECTO "Imagen" del JLabel y se establece su tama√±o contenedor
            jLFoto.setText("");
            jLFoto.setSize(71, 81);
            //Se dimensiona la IMAGEN para que tenga el mismo largo y ancho del JLabel
            Image dFotoPerfil = fotoPerfil.getScaledInstance(jLFoto.getWidth(), jLFoto.getHeight(), Image.SCALE_SMOOTH);
            //Se pone la imagen en el JLabel usando setIcon()
            jLFoto.setIcon(new ImageIcon(dFotoPerfil));
        } catch (IOException ex) {
            Logger.getLogger(PrestamosView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
